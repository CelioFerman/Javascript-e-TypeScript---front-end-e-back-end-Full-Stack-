/*Não podemos criar variáveis com palavras reservadas
Variaveis precisam ter nomes significativos
Não pode começar o nome de uma variavel com um numero
Não podem conter espaços ou traços
Utilizamos camelCase
Case-sensitive
Não podemos redeclarar variaveis com let
Não utilizar var, sempre utilize let.
*/
let name1 = "Jhon";
let name2 = "Mary";
let name3 = "Dean";

console.log(name1 + " nasceu em 1984");
console.log("Em 2000 " + name1 + " conheceu " + name2);
console.log(name1 + " casou-se com " + name2 + " em 2012");
console.log(name2 + " teve um filho com " + name1 + " em 2015");
console.log("O filho de " + name1 + " se chama " + name3);
